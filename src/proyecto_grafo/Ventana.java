    private void pasar_a_matriz_adyacencia() {
       table_panel1.eliminar_All();//elimino todo los elemento de la matrices
        Grafo g = panel.getGrafo();//saco el grafo del panel
        data_fila[] dato = new data_fila[g.size_vertice()];//hago de una arreglo de vectores 
        for (int i = 0; i < g.size_vertice(); i++) {//estructuras de datos
            dato[i] = new data_fila();
        }

        for (int i = 0; i < g.size_vertice(); i++) {
            Vertices A = g.get_vertices(i);
            dato[i].setNombre(A.getNombre());
            for (int j = 0; j < g.size_vertice(); j++) {
                if (j != i) {
                    Vertices B = g.get_vertices(j);
                    int peso =A.adyacencia(B);
                    if (peso != Integer.MIN_VALUE) {
                        dato[i].setArry(j, peso);
                    }

                }
            }
            table_panel1.add(dato[i]);
        }
    }

    private void pasar_a_matriz_incidecia() {
         this.table_incide1.eliminar_All();//elimino todo los elemento de la matrices
        Grafo g = panel.getGrafo();//saco el grafo del panel
        data_fila[] dato = new data_fila[g.size_vertice()];//hago de una arreglo de vectores 
        
        for (int i = 0; i < g.size_vertice(); i++) {//estructuras de datos
            dato[i] = new data_fila(g.get_vertices(i).getNombre());
        }
        for(int i =0;i<g.size_arista();i++){          
            String nombre = g.get_arista(i).getOrigen().getNombre();
            String nombre_0 = g.get_arista(i).getFin().getNombre();
            table_incide1.add_columnNames(nombre + "->" + nombre_0);
        }
        int tipo =this.panel.getTipo_grafo();
        if(tipo==1|tipo==2){            
           
            for (int i = 0; i < g.size_arista(); i++) {
                Arista A = g.get_arista(i);
                for (int j = 0; j < g.size_vertice(); j++) {
                    Vertices B = g.get_vertices(j);
                    if (A.getOrigen().equals(B)) {
                        dato[j].setArry(i, 1);
                    } else if (A.getFin().equals(B)) {
                         dato[j].setArry(i, 1);
                    }
                  
                }
            }

           
        }else if(tipo==3|tipo==4){
        
          for (int i = 0; i < g.size_arista(); i++) {
                Arista A = g.get_arista(i);
                for (int j = 0; j < g.size_vertice(); j++) {
                    Vertices B = g.get_vertices(j);
                    if (A.getOrigen().equals(B)) {                        
                        dato[j].setArry(i, -1);
                    } else if (A.getFin().equals(B)) {
                         dato[j].setArry(i, 1);
                    }
                  
                }
               
            }
        
        }
        for (int j = 0; j < g.size_vertice(); j++) {
            table_incide1.add(dato[j]);
        }
    }

