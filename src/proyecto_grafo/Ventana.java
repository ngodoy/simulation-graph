private void guarda_el_grafo_en_xml(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guarda_el_grafo_en_xml


    JFileChooser chooser = new JFileChooser();

    File fichero = null;
    chooser.addChoosableFileFilter(new FiltroFichero());
    chooser.setSelectedFile(new File(".xml"));
    int returnVal = chooser.showOpenDialog(this);
    if (returnVal == JFileChooser.APPROVE_OPTION) {
        fichero = chooser.getSelectedFile();
        Xml_creator generador = new Xml_creator();
        Grafo g = panel.getGrafo();
        generador.generarDocumentoXMLDescarga(g, paneles.getSelectedIndex());
        String textoXML = generador.obtenerTextoXML();
        generador.guardarDocumentoXML(textoXML, fichero);
        System.out.println(textoXML);
    }

}//GEN-LAST:event_guarda_el_grafo_en_xml
private void abre_el_grafo_del_xml(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_abre_el_grafo_del_xml

    JFileChooser chooser = new JFileChooser();
    File fichero = null;
    chooser.addChoosableFileFilter(new FiltroFichero());
    int returnVal = chooser.showOpenDialog(this);
    if (returnVal == JFileChooser.APPROVE_OPTION) {
        fichero = chooser.getSelectedFile();
        ProcesarXML procesador = new ProcesarXML(fichero);
        panel.setGrafo(procesador.parsearDocumento());

        switch (panel.getTipo_grafo()) {
            case 1:
                GND_S.setSelected(true);
                break;
            case 2:
                GND_C.setSelected(true);
                break;
            case 3:
                GD_S.setSelected(true);
                break;
            case 4:
                GD_C.setSelected(true);
                break;

        }
        switch (procesador.getRepresentacion()) {

            case 1:
                this.pasar_a_matriz_adyacencia();
                this.paneles.setSelectedIndex(1);
                break;
            case 2:
                this.pasar_a_matriz_incidecia();
                this.paneles.setSelectedIndex(2);

                break;


        }
    }

}//GEN-LAST:event_abre_el_grafo_del_xml

