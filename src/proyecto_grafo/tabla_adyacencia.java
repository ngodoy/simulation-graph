    /**
     * elimina la posicion "i" de la tabla 
     * tanto fila como columna
     * @param i
     */
    public void elimiar(int i) {
        TableColumnModel modo_coulumna = Matriz_V.getColumnModel();
        TableColumn la_columna = modo_coulumna.getColumn(i + 1);
        Matriz_V.removeColumn(la_columna);
        dataModel.removeElementAt(i);
    }

    /**
     * elimina cada uno de los elemento 
     * en la tabla y fila
     */
    public void eliminar_All() {
        int i = this.dataModel.size();
        while (i > 0) {
            i--;
            elimiar(i);

        }
    }

    
